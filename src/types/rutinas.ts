/*
|--------------------------------------------------------------------------
| CONFIGURACIÓN AVANZADA
|--------------------------------------------------------------------------
*/

export type ConfiguracionAvanzada = {

  /*
  |--------------------------------------------------------------------------
  | OVERRIDE
  |--------------------------------------------------------------------------
  */

  overrideActivo: boolean;

  seriesOverride: number | null;

  repsOverride: number | null;

  /*
  |--------------------------------------------------------------------------
  | RIR
  |--------------------------------------------------------------------------
  */

  rir: number | null;

  /*
  |--------------------------------------------------------------------------
  | TEMPO
  |--------------------------------------------------------------------------
  */

  tempo: string | null;

  /*
  |--------------------------------------------------------------------------
  | DESCANSO
  |--------------------------------------------------------------------------
  */

  descansoSegundos: number | null;

  /*
  |--------------------------------------------------------------------------
  | TIMER
  |--------------------------------------------------------------------------
  */

  usarTimer: boolean;

  /*
  |--------------------------------------------------------------------------
  | WARMUP
  |--------------------------------------------------------------------------
  */

  warmup: boolean;

  /*
  |--------------------------------------------------------------------------
  | DROPSET
  |--------------------------------------------------------------------------
  */

  dropset: boolean;

  /*
  |--------------------------------------------------------------------------
  | CLUSTER
  |--------------------------------------------------------------------------
  */

  cluster: boolean;

  /*
  |--------------------------------------------------------------------------
  | SUPERSET
  |--------------------------------------------------------------------------
  */

  superserieId: number | null;
};

/*
|--------------------------------------------------------------------------
| EJERCICIO RUTINA
|--------------------------------------------------------------------------
*/

export type EjercicioRutina = {

  id: number;

  ejercicioId: number;

  materialId: number;

  notas: string;

  configuracion:
    ConfiguracionAvanzada;
};

/*
|--------------------------------------------------------------------------
| EJERCICIO DRAFT
|--------------------------------------------------------------------------
| Ejercicio temporal antes de agregar.
|--------------------------------------------------------------------------
*/

export type EjercicioDraft = {

  ejercicioId: number;

  materialId: number;

  notas: string;

  configuracion:
    ConfiguracionAvanzada;
};

/*
|--------------------------------------------------------------------------
| DÍA
|--------------------------------------------------------------------------
*/

export type DiaRutina = {

  id: number;

  ejercicios:
    EjercicioRutina[];
};

/*
|--------------------------------------------------------------------------
| RUTINA
|--------------------------------------------------------------------------
*/

export type Rutina = {

  id: number;

  alumnoId: string;

  fechaInicio: string;

  cantidadSemanas: number;

  progresion: {

    series: number;

    reps: number;
  };

  activa: boolean;

  dias: DiaRutina[];
};