/*
|--------------------------------------------------------------------------
| SERIE REALIZADA
|--------------------------------------------------------------------------
| Lo que realmente hizo el alumno.
|--------------------------------------------------------------------------
*/

export type SerieRealizada = {

  /*
  |--------------------------------------------------------------------------
  | PESO
  |--------------------------------------------------------------------------
  */

  peso: number | null;

  /*
  |--------------------------------------------------------------------------
  | REPS
  |--------------------------------------------------------------------------
  */

  reps: number | null;

  /*
  |--------------------------------------------------------------------------
  | COMPLETADA
  |--------------------------------------------------------------------------
  */

  completada: boolean;

  /*
  |--------------------------------------------------------------------------
  | RPE OPCIONAL FUTURO
  |--------------------------------------------------------------------------
  */

  rpe?: number | null;
};

/*
|--------------------------------------------------------------------------
| EJERCICIO REALIZADO
|--------------------------------------------------------------------------
*/

export type EjercicioRealizado = {

  /*
  |--------------------------------------------------------------------------
  | IDs
  |--------------------------------------------------------------------------
  */

  ejercicioId: number;

  materialId: number;

  /*
  |--------------------------------------------------------------------------
  | SERIES
  |--------------------------------------------------------------------------
  */

  series: SerieRealizada[];

  /*
  |--------------------------------------------------------------------------
  | TIMER
  |--------------------------------------------------------------------------
  */

  timerUsado: boolean;

  /*
  |--------------------------------------------------------------------------
  | TIEMPO DESCANSO REAL
  |--------------------------------------------------------------------------
  */

  descansoRealSegundos?: number[];

  /*
  |--------------------------------------------------------------------------
  | NOTAS ALUMNO
  |--------------------------------------------------------------------------
  */

  notasAlumno: string;

  /*
  |--------------------------------------------------------------------------
  | COMPLETADO
  |--------------------------------------------------------------------------
  */

  completado: boolean;
};

/*
|--------------------------------------------------------------------------
| DÍA ENTRENADO
|--------------------------------------------------------------------------
*/

export type DiaEntrenado = {

  /*
  |--------------------------------------------------------------------------
  | DÍA RUTINA
  |--------------------------------------------------------------------------
  */

  diaId: number;

  /*
  |--------------------------------------------------------------------------
  | FECHA
  |--------------------------------------------------------------------------
  */

  fecha: string;

  /*
  |--------------------------------------------------------------------------
  | EJERCICIOS
  |--------------------------------------------------------------------------
  */

  ejercicios: EjercicioRealizado[];

  /*
  |--------------------------------------------------------------------------
  | FINALIZADO
  |--------------------------------------------------------------------------
  */

  finalizado: boolean;
};

/*
|--------------------------------------------------------------------------
| PROGRESO ALUMNO
|--------------------------------------------------------------------------
| Historial completo de entrenamientos.
|--------------------------------------------------------------------------
*/

export type ProgresoAlumno = {

  /*
  |--------------------------------------------------------------------------
  | ID ALUMNO
  |--------------------------------------------------------------------------
  */

  alumnoId: string;

  /*
  |--------------------------------------------------------------------------
  | HISTORIAL
  |--------------------------------------------------------------------------
  */

  entrenamientos:
    DiaEntrenado[];
};