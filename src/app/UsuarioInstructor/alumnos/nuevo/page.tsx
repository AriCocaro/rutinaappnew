"use client";

 /*                                                                         |
| -------------------------------------------------------------------------- |
| HOOKS                                                                      |
| -------------------------------------------------------------------------- |
| */                                                                         

import {
useState,
} from "react";

import {
useRouter,
} from "next/navigation";

 /*                                                                         |
| -------------------------------------------------------------------------- |
| TYPES                                                                      |
| -------------------------------------------------------------------------- |
| */                                                                         

import {
Alumno,
} from "@/types/alumnos";

/*                                                                         |
| -------------------------------------------------------------------------- |
| STORAGE                                                                    |
| -------------------------------------------------------------------------- |
| */                                                                         

import {
agregarAlumno,
generarAlumnoId,
} from "@/lib/alumnosStorage";

 /*                                                                         |
| -------------------------------------------------------------------------- |
| COMPONENTES                                                                |
| -------------------------------------------------------------------------- |
| */                                                                         

import FormularioAlumno from "@/components/UsuarioInstructor/Alumnos/FormularioAlumno";

 /*                                                                         |
| -------------------------------------------------------------------------- |
| COMPONENTE                                                                 |
| -------------------------------------------------------------------------- |
| */                                                                         

export default function NuevoAlumnoPage() {

 /*                                                                         |
| -------------------------------------------------------------------------- |
| ROUTER                                                                     |
| -------------------------------------------------------------------------- |
| */                                                                         

const router =
useRouter();

 /*                                                                         |
| -------------------------------------------------------------------------- |
| STATE                                                                      |
| -------------------------------------------------------------------------- |
|                                                                            |
| Estado inicial para crear un alumno.                                       |
|                                                                            |
| */                                                                         

const [
alumno,
setAlumno,
] = useState<Alumno>({


/*
|--------------------------------------------------------------------------
| IDENTIFICACIÓN
|--------------------------------------------------------------------------
*/

id: 0,

nombre: "",

apellido: "",

nrodoc: "",

/*
|--------------------------------------------------------------------------
| CONTACTO
|--------------------------------------------------------------------------
*/

email: "",

telefono: "",

direccion: "",

/*
|--------------------------------------------------------------------------
| DATOS PERSONALES
|--------------------------------------------------------------------------
*/

fechaNacimiento:
  null,

sexo:
  null,

observaciones: "",

/*
|--------------------------------------------------------------------------
| NOTAS PRIVADAS
|--------------------------------------------------------------------------
*/

notasInstructor: "",

/*
|--------------------------------------------------------------------------
| FECHAS
|--------------------------------------------------------------------------
*/

fechaAlta:
  new Date()
    .toISOString(),

activo: true,

/*
|--------------------------------------------------------------------------
| CONTENIDO
|--------------------------------------------------------------------------
*/

objetivos: [],

archivos: [],

mediciones: [],

/*
|--------------------------------------------------------------------------
| ADMINISTRACIÓN
|--------------------------------------------------------------------------
*/

asistencia: {

  vecesPorSemanaObjetivo:
    null,

  promedioSemanal:
    null,

  observaciones: "",
},

situacionEconomica: {

  estado:
    "al_dia",

  plan: "",

  ultimoPago:
    null,

  observaciones: "",
},


});

 /*                                                                         |
| -------------------------------------------------------------------------- |
| ACTUALIZAR CAMPO                                                           |
| -------------------------------------------------------------------------- |
| */                                                                         

function actualizarCampo(


campo: keyof Alumno,

valor:
  string |
  boolean |
  null


) {


setAlumno(

  (prev) => ({

    ...prev,

    [campo]:
      valor,

  })

);


}

 /*                                                                         |
| -------------------------------------------------------------------------- |
| GUARDAR                                                                    |
| -------------------------------------------------------------------------- |
| */                                                                         

function guardarAlumno() {


/*
|--------------------------------------------------------------------------
| VALIDACIONES BÁSICAS
|--------------------------------------------------------------------------
*/

if (
  !alumno.nombre.trim()
) {

  alert(
    "Ingresar nombre"
  );

  return;
}

if (
  !alumno.apellido.trim()
) {

  alert(
    "Ingresar apellido"
  );

  return;
}

/*
|--------------------------------------------------------------------------
| CREAR ALUMNO
|--------------------------------------------------------------------------
*/

const nuevoAlumno:
  Alumno = {

  ...alumno,

  id:
    generarAlumnoId(),

  fechaAlta:
    new Date()
      .toISOString(),
};

agregarAlumno(
  nuevoAlumno
);

alert(
  "Alumno creado correctamente"
);

/*
|--------------------------------------------------------------------------
| REDIRECCIÓN
|--------------------------------------------------------------------------
*/

router.push(
  "/UsuarioInstructor/alumnos"
);


}

/*                                                                         |
| -------------------------------------------------------------------------- |
| RENDER                                                                     |
| -------------------------------------------------------------------------- |
| */                                                                         

return (

<div
  className="
    p-6
    flex
    flex-col
    gap-6
  "
>

  {/* ----------------------------------------------------- */}
  {/* HEADER                                                */}
  {/* ----------------------------------------------------- */}

  <div>

    <h1
      className="
        text-3xl
        font-bold
      "
    >

      Nuevo Alumno

    </h1>

    <p
      className="
        text-gray-500
        mt-1
      "
    >

      Crear ficha inicial
      del alumno.

    </p>

  </div>

  {/* ----------------------------------------------------- */}
  {/* FORMULARIO                                            */}
  {/* ----------------------------------------------------- */}

  <FormularioAlumno

    alumno={alumno}

    onChange={
      actualizarCampo
    }

  />

  {/* ----------------------------------------------------- */}
  {/* GUARDAR                                               */}
  {/* ----------------------------------------------------- */}

  <button

    type="button"

    onClick={
      guardarAlumno
    }

    className="
      bg-green-600
      text-white
      rounded-xl
      px-5
      py-4
      font-medium
    "
  >

    Guardar Alumno

  </button>

</div>


);

}
